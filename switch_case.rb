# Chapter six switch case
# Pada dasarnya switch case adalah percabangan seperti ifelse
# di switch case hanya berjalan ketika nilai itu benar / true

# switch dengan integer
id = 1
case id
  when 1
    puts "benar"
  when 2
    puts "salah"
end

# switch dengan string
str = "Ahmad rosid"
case str
  when "Ahmad rosid"
    puts "BENAR"
end
